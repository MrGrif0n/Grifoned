#include "../include/libft.h"

//The strlen() function calculates the length of a given string.

size_t	ft_strlen(const	char *str)
{
	size_t	i;

	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		i ++;
	}
	return (i);
}
