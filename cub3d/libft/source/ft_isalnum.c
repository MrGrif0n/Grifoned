#include "../include/libft.h"

/*The isalnum() function checks whether the
 argument passed is an alphanumeric character (alphabet or number) or not.*/

int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
